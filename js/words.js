// 퍼즐 #1 = 2026-07-10 (KST)
const EPOCH_UTC_MS = Date.UTC(2026, 6, 9, 15, 0, 0); // 2026-07-10 00:00 KST == 07-09 15:00 UTC

/** 현재 시각 기준 퍼즐 번호 (1부터) */
export function getPuzzleNumber(now = Date.now()) {
  const days = Math.floor((now - EPOCH_UTC_MS) / 86400000);
  return Math.max(1, days + 1);
}

/** 퍼즐 번호 → 정답 단어 */
export function getAnswerForPuzzle(num) {
  // 선형 합동 셔플로 순서 예측 어렵게 (리스트 순서 노출 방지)
  const n = WORDS.length;
  const idx = ((num - 1) * 67 + 13) % n;
  return WORDS[idx];
}

/** 오늘의 정답 자모 */
export function getTodayAnswer(now = Date.now()) {
  const num = getPuzzleNumber(now);
  const word = getAnswerForPuzzle(num);
  return { num, word, jamo: wordToJamo(word) };
}

/** 다음 퍼즐(KST 자정)까지 남은 ms */
export function msUntilNextPuzzle(now = Date.now()) {
  const elapsed = (now - EPOCH_UTC_MS) % 86400000;
  return 86400000 - elapsed;
}
